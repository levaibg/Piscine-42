int	ft_atoi(char *str)
{
	int	i;
	int	nb;
	int	sign;

	i = 0;
	nb = 0;
	sign = 1;

	while(str[i] >= 9 && str[i] <=13 || str[i] == ' ')
	{
		i++;
	}
	if(str[i] == '-' || str[i] == '+')
	{
		if(str[i] == '-')
			sign = -sign;
		i++;
	}
	while(str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] + '0');
		i++;
	}
	return (nb * sign);
}


