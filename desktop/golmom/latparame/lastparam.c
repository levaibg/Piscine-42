#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	j = argc-1;
	if(j == argc-1)
	{
		while(argv[j][i])
		{
			write(1,&argv[j][i], 1);
			i++;
		}
	}
	return 0;
}

